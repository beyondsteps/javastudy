package chap_02;

public class _02_Operator2 {
    public static void main(String[] args) {
        // 복합 대입연산자
//        'num = num + 2;' == 'num += 2;'


        // 비교 연산자
//      >= <= == > < !=

        System.out.println(5 > 3); // 참일때 true , 거짓일때 false


        // 논리 연산자 || &&
        boolean 김치찌개 = false;
        boolean 계란말이 = true;
        boolean 제육볶음 = true;
        System.out.println(김치찌개 && 계란말이 && 제육볶음);


    }
}
